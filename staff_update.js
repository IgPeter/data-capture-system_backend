async function updateStaffCategory() {
  try {
    // Connect to MongoDB
    await mongoose.connect(process.env.userResetString12, {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });

    console.log("Connected to MongoDB");
  } catch (error) {
    console.error("Error connecting to MongoDB:", error);
  }

  try {
    // Connect to MySQL
    const mysqlConnection = await mysql.createConnection({
      host: process.env.MYSQL_HOST,
      user: process.env.MYSQL_USER,
      password: process.env.MYSQL_PASSWORD,
      database: process.env.MYSQL_DB2,
    });

    console.log("Connected to MySQL");

    // Update staff category for each staff ID
    for (const staffId of staffIdsRaw) {
      try {
        const [rows] = await mysqlConnection.execute(
          "SELECT * FROM payroll_db.payroll_sheet WHERE compno = ?",
          [staffId],
        );

        if (rows.length > 0) {
          const staffData = rows[0];
          const staffCategory = staffData.current_designation;

          // Update the staff category in MongoDB
          await mongoose.connection
            .collection("staffs")
            .updateOne({ staffId: staffId }, { $set: { staffCategory } });

          console.log(
            `Updated staff ID ${staffId} with category: ${staffCategory}`,
          );
        }
      } catch (error) {
        console.error(`Error processing staff ID ${staffId}:`, error);
      }
    }
  } catch (error) {
    console.error("Error connecting to MySQL:", error);
  }
}

updateStaffCategory();
