export const secondaryPrefixes = ["ube", "junior", "u b e", "universal"];

export const getLevelFromSchoolName = (name = "") => {
  const schoolName = name.toLowerCase();

  const isPrimary = primaryPrefixes.some((prefix) =>
    schoolName.startsWith(prefix.toLowerCase()),
  );

  const isSecondary = secondaryPrefixes.some((prefix) =>
    schoolName.startsWith(prefix.toLowerCase()),
  );

  if (isSecondary) return "secondary";
  if (isPrimary) return "primary";
  return "other";
};
